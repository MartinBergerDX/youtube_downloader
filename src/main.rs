/// Resolve a yt-dlp `--js-runtimes` value with an absolute executable path when possible.
fn resolve_js_runtime(explicit: Option<&str>) -> Option<String> {
    if let Some(runtime) = explicit {
        if runtime.contains(':') {
            return Some(runtime.to_string());
        }
        if let Some(path) = find_on_path(runtime) {
            return Some(format!("{}:{}", runtime, path.display()));
        }
        return Some(runtime.to_string());
    }

    for name in ["deno", "node"] {
        if let Some(path) = find_on_path(name) {
            return Some(format!("{name}:{}", path.display()));
        }
    }
    None
}

/// yt-dlp loads `yt-dlp.conf` from the same directory as the binary (portable config).
/// The yt-dlp crate's metadata extractors do not receive `Downloader::with_args`, so this
/// file is how we ensure `--js-runtimes` applies to fetch commands too.
fn write_ytdlp_portable_config(libraries_dir: &Path, js_runtime: &str) -> Result<(), TestError> {
    let config_path = libraries_dir.join("yt-dlp.conf");
    let content = format!(
        "# Auto-generated by youtube_downloader\n--js-runtimes {js_runtime}\n"
    );
    fs::write(&config_path, content)?;
    println!("Wrote yt-dlp config: {}", config_path.display());
    Ok(())
}

